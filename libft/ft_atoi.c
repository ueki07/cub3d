int	ft_atoi(const char *nptr, int *i)
{
	int	res;
	int	tmp;

	tmp = *i;
	res = 0;
	while (nptr[*i] >= '0' && nptr[*i] <= '9')
	{
		res = res * 10 + (nptr[*i] - '0');
		(*i)++;
	}
	if (*i == tmp)
		return (-1);
	return (res);
}
