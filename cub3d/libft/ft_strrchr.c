char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	k;
	char	*res;

	i = -1;
	k = (char)c;
	while (s[++i])
		;
	i++;
	res = (char *)s;
	while (i-- > 0)
		if (s[i] == k)
			return (res + i);
	return (00);
}
