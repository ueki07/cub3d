#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned int	i;
	char			*s1;

	s1 = (char *)s;
	i = 0;
	while (i++ < n)
		s1[i - 1] = c;
	return (s1);
}
